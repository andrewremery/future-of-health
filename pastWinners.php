            	<p>Benjamin L. Miller is a Professor of Dermatology, Biochemistry and Biophysics, and Biomedical Engineering
at the University of Rochester, where he also co-directs the Innovative Science Program in Biomedical
Nanotechnology. Raised in southwestern Ohio, Ben carried out his undergraduate studies at
Miami University, receiving degrees in Chemistry (B.S.), Mathematics (A.B.), and German (A.B.) in 1988.
After acquiring a Ph. D. in Chemistry from Stanford University and carrying out postdoctoral research at
Harvard University, Ben joined the faculty of the University of Rochester in 1996. Research in his group
has included projects directed towards methods of non-biopolymeric molecular evolution, molecular
recognition, computer-aided molecular design, synthetic methodology, and materials science. Most recently,
Ben has focused on using his group’s expertise in molecular recognition chemistry, nano-technology,
and chip-based bio-molecular sensing systems to aid in the development of novel bio-sensors
and rapid diagnostic tools, as well as continuing to attack the problem of sequence-selective RNA
recognition. He is an active entrepreneur, and is a founder of Adarza Biosystems, Inc., a Rochester
area biomedical start-up company.</p>
