            	<p>Dr. Kevin Warwick is the first Cyborg and Professor of Cybernetics at the University of Reading, UK where he carries out research in artificial intelligence, control and robotics.
His favourite topic is pushing back the frontiers of machine intelligence.
Kevin began his career by joining British Telecom.   At age 22 he took his first degree at Aston University followed by a PhD and research post at Imperial College, London.
He subsequently held positions at Oxford, Newcastle and Warwick Universities before being offered the Chair at Reading, at the age of 32.
Kevin has published over 350 research papers and his paperback “In the Mind of the Machine” gives a warning of a future in which machines are more intelligent than humans.
He has been awarded higher doctorates both by Imperial College and the Czech Academy of Sciences. He has been described by Gillian Anderson of the X-Files as Britain’s leading prophet of the robot age.
He appears in the 1999 Guinness Book of Records for an Internet robot learning experiment.  In 1998 he had a silicon chip transponder surgically implanted in his left arm and then performed series of further implant experiments linking Kevin’s nervous system to a computer.  This research led to him being featured in February 2000, as the cover story on the US magazine “Wired”. He received the Future Health Technology Award in the year 2000.</p>
