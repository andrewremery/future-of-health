        	<h2>The 2016 FHTI Award Recipient</h2>
            <h2>Dr. Xui-Min Li</h2>
 <p>     Dr. Xiu-Min Li, Director, Center for Integrative Medicine for Allergies and Wellness and Professor of Pediatric Allergy and Immunology, Mount Sinai Hospital, has been named the recipient of the 2016 Future of Health Technology Award for her research and clinical work on botanical drugs for asthma, food allergies, and other diseases of the immune system, and for her vision to make them available to patients through integrative medical practice. Allergic diseases represent a growing threat to life and quality of life in developed nations and are spreading as more countries strive for Western living standards. According to American College of Allergy, Asthma and Immunology there are approximately 50 million people with allergies in US.</p>          
 <p>     The research projects of Dr. Li and her collaborators have provided major insights into the immune system with the goal of creating safe botanical drugs for food allergies, eczema, allergic rhinitis and asthma and, in the future, potentially for irritable bowel disease (IBD) and weight control, without the cytotoxicity, depression, and anxiety that frequently accompany mainstream treatment.</p>
<p>     Her work also uncovered potential early intervention for preventing food allergy and asthma altogether. Facilitated by a dual regulatory structure, which allows botanical drugs to be used as supplements even while research on investigatory drugs continues, her novel eczema, food allergy, and asthma treatments are already helping patients in her private clinical practice. One of her many patients, Christopher Burch, Founder of Burch Creative Capital calls her “the Healing Angel”. “Dr. Li helps patients who cannot be helped by other doctors” said Barbara Winston, President of Bruce Winston Gem</p>
<p>    Corporation, familiar with Dr. Li’s excellent clinical outcomes. The clinical experience also helps inform the laboratory science. Dr. Li has the vision of a broad collaboration to manufacture research-proven and evidence-based Chinese herbal medicines, and make them widely available through a practice network of trained allopathic physicians.</p>

	<p>Dr. Li obtained her MD degree in Henan
School of Chinese Medicine and a Master’s
degree in Clinical Pediatric Immunology from
The Graduate School of China Academy of
Chinese Medical Sciences, where she was
trained in integrative Western and Traditional
Chinese Medicine.</p>
<p>She was Visiting Scientist at Stanford
before completing a postdoctoral fellowship
in Clinical Immunology at the Johns Hopkins
School of Medicine, Asthma and Allergy Center,
and was appointed instructor. Dr. Li was
recruited to the Division of Pediatric Allergy
and Immunology at Mount Sinai when the
division was established in 1997.</p>
<p>In the past 17 years, she has investigated
novel therapies for asthma and food allergy
combining expertise in allergy and immunology,
molecular biology and Chinese herbal
medicine.</p>
<p>She is sponsor of US-FDA approved clinical
studies of investigational new botanical
drugs. In addition to the clinical studies, she
has established immunobiology and botanical
chemistry laboratories. Her research has
resulted in nearly 100 publications.</p>
<p>She has given 2009 Gail G. Shapiro Memorial
Lecture at the American Academy of
Allergy, Asthma and Immunology. Dr. Li also
served as vice Chair and then Chair of the
American Academy of Allergy Asthma and
Immunology (AAAAI), Complementary and Alternative
Medicine Practices in Allergy Committee.
Dr. Li also served as Chair of the First
East-West Scientific Conference on Allergy &amp;
Traditional Medicine.</p>
<p>Her work is featured in Henry Ehrlich’s
book “Food Allergies: Traditional Chinese
Medicine, Western Science, and the Search
for a Cure”.</p>
