            <p class="centerParagraphs">The Future of Health Technology Institute founded by Renata Bushko in 1996 - FHTI - is a think-tank focusing on key technologies and key challenges to the future of global healthcare. Our goal is to bring together leading thinkers in the area of newly emerging technology and health and to redefine world health strategy so that technology investments can result in less disease and better, affordable treatments. We look for most transformative inventions, ideas, and research results to make lives happier and longer and we disseminate them to maximize their positive impact e.g. 20th Anniversary World Health Strategy eBook (Ed.) Renata G. Bushko.</p>
