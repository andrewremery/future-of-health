         	 <h2>FHTI summit</h2>
             <h3>Mission</h3>
            <p class="centerParagraphs">Define the health technology strategy for the 21st century. It is a 20th Anniversary event that brings together leading health and technology experts to reveal, share and debate ideas about cutting-edge medical technologies
being utilized to save lives, reduce suffering and extend human potential and to debate world health strategy.
Discover new collaborations and ideas to influence health technology for the 21st century to make lives happier
and longer. It is an annual event that brings together leading health and technology experts to reveal, share,
debate and create ideas involving cutting-edge medical technologies to save lives, reduce suffering and extend
human potential. It is a unique, one-of-a-kind interdisciplinary innovation meeting including inventors, academia,
industry, media, arts, and investors.</p>
