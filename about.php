    <h2>About the Future of Health Technology Institute</h2>
    <h3>History</h3>
    <hr>
    <p>The Future of Health Technology Institute (FHTI) was founded in 1996 to address
the long-term issues of health technology. FHTI is a think-tank aimed at defining
an agenda for health technology development and determining the most critical
focus areas for health technology investment in the new century. Founded on the
40th anniversary of the field of Artificial Intelligence, FHTI found inspiration in the
intellectual legacy of Professor Marvin Minsky. He is one of the founders of Artificial
Intelligence and one of the most creative minds of our times. I became his student
in 1985 when he was working on the Society of Mind theory.</p>
<P>Since its foundation in 1996, FHTI has been doing what Professor Dertouzos
describes as: “Put in a salad bowl the wildest, most forward-thinking technological
ideas that you can imagine. Then add your best sense of what will be useful to
people. Start mixing the salad. Something will pop up that begins to qualify on both
counts”. Professor Dertouzos suggests that we should then “Grab it and run with it,”
which agrees with the paradigm that the best way to define the future is to invent it.</P>
<P>Unfortunately this optimistic model leaves a lot to random chance. The salad-
bowl approach is a good start, but each idea should be attributed impact value
and compared against alternative ideas. The ideas rely on both technological
novelty and human usefulness. The process of comparing various ideas can be an
efficient way to save time and money. FHTI proposes to manage the coordination
of the most promising ideas in health technology using top-level experts from the
industry, government and academia during Future of Health Technology Summits.</P>
    <h3>Goals</h3>
    <hr>
    <uL>
    	<li>Develop a vision of future health care supported by current and future health
technologies.</li>
        <li>Define distinct promising health technology research areas.</li>
        <li>Demonstrate that technology driven cost increases in healthcare can be
stopped and possibly reversed by a new allocation of research and development
resources.</li>
        <li>Define productive areas for research and development that will have potential
impact on healthcare.</li>
        <li>Identify new technologies that are practical and necessary in health and wellness
maintenance.</li>
        <li>Identify research and development needed to meet future health challenges.</li>
        <li>Identify current products best for preparing 21st century healthcare.</li>
        <li>Empower consumers.</li>
    </uL>
